void Caes256::miyao(unsigned char kk[4][8])//密钥并扩展
{
	unsigned char c[4],cc,rc[14]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,
		                          0x80,0x1b,0x36,0x6c,0xd8,0xab,0x4d};//轮常量
	int i,j,k;
	for(j=0;j<8;j++)
			for(k=0;k<4;k++)
				key[0][k][j]=kk[k][j];
	for(i=1;i<15;i++)//扩展14个密钥
	{
		for(j=0;j<4;j++)
			c[j]=key[i-1][j][7];
		cc=she[c[0]];
		c[0]=she[c[1]];
		c[1]=she[c[2]];
		c[2]=she[c[3]];
		c[3]=cc;
		key[i][0][0]=key[i-1][0][0]^c[0]^rc[i-1];
		key[i][1][0]=key[i-1][1][0]^c[1];
		key[i][2][0]=key[i-1][2][0]^c[2];
		key[i][3][0]=key[i-1][3][0]^c[3];
		for(j=1;j<8;j++)
			for(k=0;k<4;k++)
				key[i][k][j]=key[i-1][k][j]^key[i][k][j-1];
	}
}

void Caes256::jiamik(unsigned char k[4][8])//加密单块
{
	unsigned char c,t[4];
	int i,j,q,r,cc;
	for(i=0;i<4;i++)//密钥加变换
		for(j=0;j<8;j++)
			k[i][j]=k[i][j]^key[0][i][j];
	for(q=1;q<14;q++)
	{
		for(j=0;j<8;j++)//S盒加行位移变换
			k[0][j]=she[k[0][j]];
		c=she[k[1][7]];k[1][7]=she[k[1][0]];//第二行左移1
		k[1][0]=she[k[1][1]];k[1][1]=she[k[1][2]];
		k[1][2]=she[k[1][3]];k[1][3]=she[k[1][4]];
		k[1][4]=she[k[1][5]];k[1][5]=she[k[1][6]];k[1][6]=c;
		c=she[k[2][0]];k[2][0]=she[k[2][3]];//第三行左移3
		k[2][3]=she[k[2][6]];k[2][6]=she[k[2][1]];
		k[2][1]=she[k[2][4]];k[2][4]=she[k[2][7]];
		k[2][7]=she[k[2][2]];k[2][2]=she[k[2][5]];k[2][5]=c;
		c=she[k[3][0]];k[3][0]=she[k[3][4]];k[3][4]=c;//第四行左移4
		c=she[k[3][1]];k[3][1]=she[k[3][5]];k[3][5]=c;
		c=she[k[3][2]];k[3][2]=she[k[3][6]];k[3][6]=c;
		c=she[k[3][3]];k[3][3]=she[k[3][7]];k[3][7]=c;
		for(cc=0;cc<8;cc++)//列混淆变换
		{
			for(r=0;r<4;r++)
			    t[r]=k[r][cc];
			for(r=0;r<4;r++)
			{
			    k[r][cc]=FFmul(0x02,t[r])
			            ^FFmul(0x03,t[(r+1)%4])
			            ^FFmul(0x01,t[(r+2)%4])
			            ^FFmul(0x01,t[(r+3)%4]);
			}
		}
		for(i=0;i<4;i++)//密钥加变换
			for(j=0;j<8;j++)
				k[i][j]=k[i][j]^key[q][i][j];
	}
	for(j=0;j<8;j++)//S盒加行位移变换
		k[0][j]=she[k[0][j]];
	c=she[k[1][7]];k[1][7]=she[k[1][0]];//第二行左移1
	k[1][0]=she[k[1][1]];k[1][1]=she[k[1][2]];
	k[1][2]=she[k[1][3]];k[1][3]=she[k[1][4]];
	k[1][4]=she[k[1][5]];k[1][5]=she[k[1][6]];k[1][6]=c;
	c=she[k[2][0]];k[2][0]=she[k[2][3]];//第三行左移3
	k[2][3]=she[k[2][6]];k[2][6]=she[k[2][1]];
	k[2][1]=she[k[2][4]];k[2][4]=she[k[2][7]];
	k[2][7]=she[k[2][2]];k[2][2]=she[k[2][5]];k[2][5]=c;
	c=she[k[3][0]];k[3][0]=she[k[3][4]];k[3][4]=c;//第四行左移4
	c=she[k[3][1]];k[3][1]=she[k[3][5]];k[3][5]=c;
	c=she[k[3][2]];k[3][2]=she[k[3][6]];k[3][6]=c;
	c=she[k[3][3]];k[3][3]=she[k[3][7]];k[3][7]=c;
	for(i=0;i<4;i++)//密钥加变换
		for(j=0;j<8;j++)
			k[i][j]=k[i][j]^key[14][i][j];
}

void Caes256::jiemik(unsigned char k[4][8])//解密单块
{
	unsigned char c,t[4];
	int i,j,q,r,cc;
	for(i=0;i<4;i++)//密钥加变换
		for(j=0;j<8;j++)
			k[i][j]=k[i][j]^key[14][i][j];
	for(j=0;j<8;j++)//逆S盒加逆行位移变换
		k[0][j]=ishe[k[0][j]];
	c=ishe[k[1][7]];k[1][7]=ishe[k[1][6]];//第二行右移1
	k[1][6]=ishe[k[1][5]];k[1][5]=ishe[k[1][4]];
	k[1][4]=ishe[k[1][3]];k[1][3]=ishe[k[1][2]];
	k[1][2]=ishe[k[1][1]];k[1][1]=ishe[k[1][0]];k[1][0]=c;
	c=ishe[k[2][0]];k[2][0]=ishe[k[2][5]];//第三行右移3
	k[2][5]=ishe[k[2][2]];k[2][2]=ishe[k[2][7]];
	k[2][7]=ishe[k[2][4]];k[2][4]=ishe[k[2][1]];
	k[2][1]=ishe[k[2][6]];k[2][6]=ishe[k[2][3]];k[2][3]=c;
	c=ishe[k[3][0]];k[3][0]=ishe[k[3][4]];k[3][4]=c;//第四行右移4
	c=ishe[k[3][1]];k[3][1]=ishe[k[3][5]];k[3][5]=c;
	c=ishe[k[3][2]];k[3][2]=ishe[k[3][6]];k[3][6]=c;
	c=ishe[k[3][3]];k[3][3]=ishe[k[3][7]];k[3][7]=c;
	for(q=13;q>0;q--)
	{
		for(i=0;i<4;i++)//密钥加变换
			for(j=0;j<8;j++)
				k[i][j]=k[i][j]^key[q][i][j];
		for(cc=0;cc<8;cc++)//逆列混淆变换
		{
			for(r=0;r<4;r++)
			    t[r]=k[r][cc];
			for(r=0;r<4;r++)
			{
			    k[r][cc]=FFmul(0x0e,t[r])
			            ^FFmul(0x0b,t[(r+1)%4])
			            ^FFmul(0x0d,t[(r+2)%4])
			            ^FFmul(0x09,t[(r+3)%4]);
			}
		}
		for(j=0;j<8;j++)//逆S盒加逆行位移变换
			k[0][j]=ishe[k[0][j]];
		c=ishe[k[1][7]];k[1][7]=ishe[k[1][6]];//第二行右移1
		k[1][6]=ishe[k[1][5]];k[1][5]=ishe[k[1][4]];
		k[1][4]=ishe[k[1][3]];k[1][3]=ishe[k[1][2]];
		k[1][2]=ishe[k[1][1]];k[1][1]=ishe[k[1][0]];k[1][0]=c;
		c=ishe[k[2][0]];k[2][0]=ishe[k[2][5]];//第三行右移3
		k[2][5]=ishe[k[2][2]];k[2][2]=ishe[k[2][7]];
		k[2][7]=ishe[k[2][4]];k[2][4]=ishe[k[2][1]];
		k[2][1]=ishe[k[2][6]];k[2][6]=ishe[k[2][3]];k[2][3]=c;
		c=ishe[k[3][0]];k[3][0]=ishe[k[3][4]];k[3][4]=c;//第四行右移4
		c=ishe[k[3][1]];k[3][1]=ishe[k[3][5]];k[3][5]=c;
		c=ishe[k[3][2]];k[3][2]=ishe[k[3][6]];k[3][6]=c;
		c=ishe[k[3][3]];k[3][3]=ishe[k[3][7]];k[3][7]=c;
	}
	for(i=0;i<4;i++)//密钥加变换
		for(j=0;j<8;j++)
			k[i][j]=k[i][j]^key[0][i][j];
}

unsigned char Caes256::FFmul(unsigned char a, unsigned char b)//有限域GF(2^8)上的乘法
{
    unsigned char bw[4];
    unsigned char res=0;
    int i;
    bw[0]=b;
    for(i=1;i<4;i++)
    {
        bw[i]=bw[i-1]<<1;
        if(bw[i-1]&0x80)
			bw[i]^=0x1b;
    }
    for(i=0;i<4;i++)
    {
        if((a>>i)&0x01)
			res^=bw[i];
    }
    return res;
}